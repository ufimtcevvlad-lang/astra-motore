// ─── Основная логика ─────────────────────────────────────────────────

function main() {
  let ts = fs.readFileSync(PRODUCTS_TS, "utf8");

  const idRe = /id:\s*"([^"]+)"/g;
  const idPositions = [];
  let m;
  while ((m = idRe.exec(ts)) !== null) {
    idPositions.push({ id: m[1], start: m.index });
  }

  const products = [];
  for (let i = 0; i < idPositions.length; i++) {
    const blockStart = idPositions[i].start;
    const blockEnd = i + 1 < idPositions.length ? idPositions[i + 1].start : ts.length;
    const slice = ts.slice(blockStart, blockEnd);

    const skuM = slice.match(/sku:\s*"([^"]+)"/);
    const nameM = slice.match(/name:\s*"([^"]+)"/);
    const categoryM = slice.match(/category:\s*"([^"]+)"/);
    const brandM = slice.match(/brand:\s*"([^"]+)"/);
    const hasLongDesc = /longDescription:\s*\{/.test(slice) && /purpose:/.test(slice);

    if (!skuM || !nameM) continue;

    const rawName = nameM[1];
    products.push({
      id: idPositions[i].id,
      sku: skuM[1],
      name: rawName,
      shortName: rawName.split("|")[0].trim(),
      category: categoryM ? categoryM[1] : "",
      brand: brandM ? brandM[1] : "",
      hasLongDesc,
      blockStart,
      blockEnd,
    });
  }

  const missing = products.filter((p) => !p.hasLongDesc);

  log(colorize(`\n📝 Генератор SEO-описаний`, "cyan"));
  log(colorize(`   Режим: ${APPLY ? "APPLY" : "DRY-RUN"}`, "gray"));
  log(`   Всего товаров: ${products.length}`);
  log(`   С описанием: ${products.length - missing.length}`);
  log(colorize(`   Без описания: ${missing.length}\n`, missing.length > 0 ? "yellow" : "green"));

  if (missing.length === 0) {
    log("Все товары уже имеют longDescription.");
    return;
  }

  for (const p of missing) {
    const template = TEMPLATES[p.category] || DEFAULT_TEMPLATE;
    const desc = template(p);
    log(`${colorize(p.sku, "cyan")} [${p.category}] → ${p.id}`);
    log(colorize(`   ${desc.purpose.slice(0, 80)}...`, "gray"));
  }

  if (!APPLY) {
    log(colorize(`\nDRY-RUN. Для записи: node scripts/generate-seo-content.mjs --apply\n`, "yellow"));
    return;
  }

  let updatedTs = ts;
  let offset = 0;

  for (const p of missing) {
    const template = TEMPLATES[p.category] || DEFAULT_TEMPLATE;
    const desc = template(p);

    const adjustedStart = p.blockStart + offset;
    const adjustedEnd = p.blockEnd + offset;
    const block = updatedTs.slice(adjustedStart, adjustedEnd);

    const lastBraceIdx = block.lastIndexOf("},");
    if (lastBraceIdx === -1) continue;

    const esc = (s) => s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');

    const insertion = `    longDescription: {\n      purpose:\n        "${esc(desc.purpose)}",\n      symptoms:\n        "${esc(desc.symptoms)}",\n      interval:\n        "${esc(desc.interval)}",\n      install:\n        "${esc(desc.install)}",\n    },\n  `;

    const insertPos = adjustedStart + lastBraceIdx;
    updatedTs = updatedTs.slice(0, insertPos) + insertion + updatedTs.slice(insertPos);
    offset += insertion.length;
  }

  fs.writeFileSync(PRODUCTS_TS, updatedTs, "utf8");
  log(colorize(`\n🟢 Добавлено описаний: ${missing.length}`, "green"));
}

main();
